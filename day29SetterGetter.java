
package day5;

public class day29SetterGetter {

    private String nama;
    private String warna;
    private int berbiji;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public int getBerbiji() {
        return berbiji;
    }

    public void setBerbiji(int berbiji) {
        this.berbiji = berbiji;
    }
}

